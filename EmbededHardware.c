void moveMotor1(bool direction, int pwm)
{
	if(pwm>255) pwm = 255;
	if(pwm<0) pwm = 0;
//	int32_t duty1;
	int duty2;
//	duty1 = (pwm*4294967295)/255;
	duty2 = (pwm*65535)/255;
//	duty = pwm*65535/255;
	if (direction == true)
	{
		__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_3, duty2);
		__HAL_TIM_SetCompare(&htim14, TIM_CHANNEL_1, 0);
	} else
	{
		__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_3, 0);
		__HAL_TIM_SetCompare(&htim14, TIM_CHANNEL_1, duty2);
	}
	sprintf(buffer, "PWM Value1: %d\n", duty2);
	uprintf(buffer);
}

